export interface ParsedPattern {
  seq: string;
  pred: 'PLAYER' | 'BANKER';
}

export function getParsedPatterns(): ParsedPattern[] {
  const lines = RAW_PATTERNS_STRING.trim().split('\n');
  const results: ParsedPattern[] = [];

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('//')) continue;
    
    const parts = trimmed.split('=');
    if (parts.length !== 2) continue;
    
    const rawSeq = parts[0];
    const rawPred = parts[1];
    
    // Map: A -> P (PLAYER), V -> B (BANKER), E -> T (TIE)
    const seq = rawSeq
      .replace(/A/g, 'P')
      .replace(/V/g, 'B')
      .replace(/E/g, 'T');
      
    // Pred is mapping V -> BANKER, A -> PLAYER. (Ignoring TIE predictions since signals are strictly PLAYER/BANKER)
    const pred = rawPred === 'A' ? 'PLAYER' : rawPred === 'V' ? 'BANKER' : null;
    
    if (seq && pred) {
      results.push({ seq, pred });
    }
  }
  
  return results;
}
